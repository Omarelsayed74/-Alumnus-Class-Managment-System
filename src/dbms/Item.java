package dbms;

/**
 *
 * @author h.d
 */
public class Item {
    private String name;
    private static int id = 0;
    public Item(String name) {
        id++;
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    @Override
    public String toString() {
        return name;
    }
}
